Hvert år har danske politikere lang sommerferie. Dukkepartiet foreslår, at fire uger i ferien bruges på erhvervspraktik. 
Dukkepartiet foreslår: Alle politikere skal i 4 ugers erhvervspraktik
Det gør vi fordi: Mange politikere har lange universitetsuddannelser: cand.jur., cand.scient.pol. og cand.oecon. og har aldrig haft et job uden for politik. Politikere repræsenterer ikke det mangfoldige danske arbejdsmarked og lovgiver dermed for et arbejdsmarked, de ikke er i berøring med.
Erhvervspraktikken skal genetablere kontakten til arbejdsmarkedet og give politikerne indsigt i, hvad det vil sige at have et job uden for politik.
