			<p>LOVFORSLAG #2: DECENTRALISERING AF MAGTEN
Centraladministrationen og de forskellige ministerier ligger i dag alle i København. Det bringer politikere og embedsmænd for langt væk fra danskere, der ikke bor i hovedstaden. Derfor foreslår Dukkepartiet: Opløs magtcentraliseringen og bring politikerne tættere på borgerne ved at flytte ministerierne til 'Udkantsdanmark'.
 
Det gør vi fordi:
De danske yderområder, det såkaldte ’Udkantsdanmark’, er beklageligt langt væk fra den politiske administration.

Toppolitikerne og -embedsmænd skal se, hvordan folk bor og lever i Nakskov, hvordan hverdagen er i Vojens, og hvordan det er at have børn i institutioner i Thisted. Ved at flytte departementerne ud af København vil man sende et signal om, at demokratiet er for alle - også for dem, der ikke bor i de store byer. 
Placeringen vil desuden bidrage til de lokale økonomier ved at bringe højtlønnede politikere og embedsfolk til økonomisk trængte områder.
Deltag i debatten på www.facebook.com/dukkepartiet</p>
		</li>
		<li class="item">
			<p>LOVFORSLAG #3 FORBUD MOD PARTIHOP: Det er i dag op til den enkelte folketingspolitiker at forvalte sit mandat, som han/hun ønsker. Det medfører, at flere politikere skifter parti midt i valgperioden. Vælgerne kan som resultat ikke længere regne med de folkevalgtes partipolitiske ståsted, og det undergraver troværdigheden. Derfor foreslår Dukkepartiet: Det skal være forbudt at skifte parti i en valgperiode.

Det gør vi fordi: At stille op for et parti indebærer ikke bare et ansvar over for partiet men også over for vælgerne. Dukkepartiet finder det i strid med demokratiets idealer at hoppe parti midt i valgperioden, fordi vælgerne simpelthen ikke ved, hvad den pågældende politiker står for. Politikere kan op til et valg selv beslutte, hvilket parti, de vil repræsentere, men beslutningen bør i valgperioden være bindende.
Deltag i debatten på www.facebook.com/dukkepartiet</p>
